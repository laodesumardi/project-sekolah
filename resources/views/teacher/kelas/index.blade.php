}

// Auto-refresh every 10 minutes
setInterval(function() {
    // Only refresh if no filters are applied
    if (!document.getElementById('class-filter').value && 
        !document.getElementById('subject-filter').value) {
        location.reload();
    }
}, 600000); // 10 minutes
</script>
